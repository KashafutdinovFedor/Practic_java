public class operator_conditional_jump_Practic_1 {
    public static void main(String[] args) {
        // Пример значения для проверки
        int a = 2;

        // Код с использованием тернарного оператора
        int i = (a == 1) ? 1 : (a == 2) ? 2 : 3;

        // Вывод результата для проверки
        System.out.println("Значение i: " + i);
    }
}
