public class A {
    // Константа объявлена, но не инициализирована сразу
    public final int a;

    // Вариант 1: Инициализация через конструктор
    // Это позволяет задавать значение константы индивидуально для каждого объекта
    public A(int value) {
        this.a = value;
    }

    // Вариант 2: Инициализация через блок инициализации экземпляра
    // Этот код выполняется при создании любого объекта класса до конструктора
    /*
    {
        a = 100;
    }
    */
}

class main {
    public static void main(String[] args) {
        // Пример вызова через конструктор
        A obj1 = new A(10);
        A obj2 = new A(20);

        System.out.println("Значение a в obj1: " + obj1.a);
        System.out.println("Значение a в obj2: " + obj2.a);
    }
}